public class OTP_03_Literals {
    public static void main(String[] args) {
        // A constant value which can be assigned to the variable is
        // called as a literal.
        byte age = 45;
        int age2 = 56;
        short age3 = 18;
        long ageDino = 45647657585657L; // here 'L' is compulsory
        char ch = 'K';
        float f1 = 5.6f; // here 'f' is compulsory
        double d1 = 3.44; // here 'd' is optional
        boolean ans = true;
        String name = "Krish";

        System.out.println(age);
        System.out.println(age2);
        System.out.println(age3);
        System.out.println(ageDino);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d1);
        System.out.println(ans);
        System.out.println(name);
    }
}
